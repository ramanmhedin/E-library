<?php

namespace App\Filament\Resources;

use App\Filament\Resources\SubjectResource\Pages;
use App\Filament\Resources\SubjectResource\RelationManagers;
use App\Models\College;
use App\Models\Department;
use App\Models\Subject;
use Filament\Forms;
use Filament\Forms\Components\Select;
use Filament\Forms\Components\TextInput;
use Filament\Forms\Form;
use Filament\Forms\Get;
use Filament\Resources\Resource;
use Filament\Tables;
use Filament\Tables\Actions\EditAction;
use Filament\Tables\Actions\ViewAction;
use Filament\Tables\Columns\TextColumn;
use Filament\Tables\Filters\SelectFilter;
use Filament\Tables\Table;
use Illuminate\Database\Eloquent\Builder;
use Illuminate\Database\Eloquent\Model;
use Illuminate\Database\Eloquent\SoftDeletingScope;
use Illuminate\Support\Collection;
use function PHPUnit\Framework\isNull;

class SubjectResource extends Resource
{
    protected static ?string $model = Subject::class;

    protected static ?string $navigationIcon = 'heroicon-o-book-open';
    protected static ?string $navigationGroup="Academic";

    public static function canAccess(): bool
    {
        return auth()->user()->role->name == "Admin" || auth()->user()->role->name == "President"; // TODO: Change the autogenerated stub
    }

    public static function canCreate(): bool
    {
        return auth()->user()->role->name == "Admin"; // TODO: Change the autogenerated stub
    }

    public static function canDelete(Model $record): bool
    {
        return auth()->user()->role->name == "Admin"; // TODO: Change the autogenerated stub
    }

    public static function canEdit(Model $record): bool
    {
        return auth()->user()->role->name == "Admin"; // TODO: Change the autogenerated stub
    }
    public static function getEloquentQuery(): Builder
    {
        if(auth()->user()->role_id==4){
            return parent::getEloquentQuery()->with(["department", "department.college"])->where("college_id",auth()->user()->college_id);
        }
        return parent::getEloquentQuery()
           ->with(["department", "department.college"]);
    }

    public static function form(Form $form): Form
    {
        return $form
            ->schema([
                TextInput::make("name")
                    ->string()
                    ->required(),
                TextInput::make("description")
                    ->string()
                    ->required(),

                Select::make("academic_degree")
                    ->label("academic_degree")
                    ->options([
                        'diploma'=>'diploma',
                        'bachelor'=>'bachelor',
                        'master'=>'master',
                        'doctoral'=>'doctoral',
                    ])
                    ->required()
                    ->searchable(),

                Select::make("college_id")
                    ->label("college")
                    ->options(College::all()->pluck("name", "id"))
                    ->reactive()
                    ->searchable(),

                Select::make("department_id")
                    ->label("department")
                    ->options(fn(Get $get): Collection => Department::query()
                        ->when($get('collage_id'),function ($query) use ($get) {
                            return $query->where("college_id", $get('college_id'));
                        })
                        ->pluck('name', 'id')
                    )
                    ->required()
                    ->searchable(),
            ]);
    }

    public static function table(Table $table): Table
    {
        return $table
            ->columns([
                TextColumn::make('name')
                    ->searchable()
                    ->sortable(),
                TextColumn::make('academic_degree')
                    ->searchable()
                    ->sortable(),
                TextColumn::make('department.name'),
                TextColumn::make('college.name'),
            ])
            ->filters([

                SelectFilter::make('college_id')
                    ->relationship("college","name")
                    ->searchable(),

                SelectFilter::make('department_id')
                    ->options(function ($livewire) {
                        return Department::query()->get()->pluck("name", "id");
                    }),

                SelectFilter::make('academic_degree')
                    ->options([
                        'diploma'=>'diploma',
                        'bachelor'=>'bachelor',
                        'master'=>'master',
                        'doctoral'=>'doctoral',
                        ]),

            ])
            ->actions([
                ViewAction::make('view')
                    ->button(),

                EditAction::make('edit')
                    ->button(),

                Tables\Actions\DeleteAction::make('delete')
                    ->button(),            ])
            ->bulkActions([
                Tables\Actions\BulkActionGroup::make([
                    Tables\Actions\DeleteBulkAction::make(),
                ]),
            ]);
    }

    public static function getRelations(): array
    {
        return [
            //
        ];
    }

    public static function getPages(): array
    {
        return [
            'index' => Pages\ListSubjects::route('/'),
            'create' => Pages\CreateSubject::route('/create'),
            'edit' => Pages\EditSubject::route('/{record}/edit'),
        ];
    }
}
